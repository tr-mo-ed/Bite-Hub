# ???? ???? get_owner_cafe ?????? ????? ?????? ?? ????? ????.
def get_owner_cafe(user):
    if user.is_superuser:
        return None
    return getattr(user, 'my_cafe', None)


# ???? ???? CafeAdmin ???? ?????? ????????? ???? ???? ?????.
@admin.register(Cafe)
class CafeAdmin(admin.ModelAdmin):
    # ??? ??????? list_display ??? ????? ??? ???? ???? ???? ????? ????.
    list_display = ('name', 'location', 'is_active', 'owner', 'faculty')
    # ??? ??????? search_fields ??? ????? ??? ???? ???? ???? ????? ????.
    search_fields = ('name', 'code')
    # ??? ??????? list_filter ??? ????? ??? ???? ???? ???? ????? ????.
    list_filter = ('is_active', 'faculty')
    # ??? ??????? fields ??? ????? ??? ???? ???? ???? ????? ????.
    fields = ('name', 'code', 'faculty', 'image', 'location', 'is_active', 'owner')

    # ???? ???? get_queryset ?????? ????? ?????? ?? ????? ????.
    def get_queryset(self, request):
        # ??? ??????? qs ??? ????? ??? ???? ???? ???? ????? ????.
        qs = super().get_queryset(request).select_related('owner', 'faculty')
        if request.user.is_superuser:
            return qs
        return qs.filter(owner=request.user)

    # ???? ???? save_model ?????? ????? ?????? ?? ????? ????.
    @transaction.atomic
    def save_model(self, request, obj, form, change):
        try:
            if change:
                # ??? ??????? existing_cafe ??? ????? ??? ???? ???? ???? ????? ????.
                existing_cafe = Cafe.objects.select_related('owner').filter(pk=obj.pk).first()
                previous_owner = existing_cafe.owner if existing_cafe is not None else None
                super().save_model(request, obj, form, change)
                initialize_cafe_runtime(obj, previous_owner=previous_owner)
                return

            # ??? ??????? created_cafe ??? ????? ??? ???? ???? ???? ????? ????.
            created_cafe = provision_cafe(
                name=obj.name,
                code=obj.code,
                faculty_id=obj.faculty_id,
                owner_id=obj.owner_id,
                location=obj.location,
                image=obj.image,
                is_active=obj.is_active,
            )

            obj.pk = created_cafe.pk
            obj.code = created_cafe.code
            obj.owner = created_cafe.owner
            obj.faculty = created_cafe.faculty
            obj.location = created_cafe.location
            obj.is_active = created_cafe.is_active
            obj.image = created_cafe.image
        except Exception as exc:
            raise ValidationError(str(exc)) from exc


# ???? ???? CategoryAdmin ???? ?????? ????????? ???? ???? ?????.
@admin.register(Category)
class CategoryAdmin(admin.ModelAdmin):
    # ??? ??????? list_display ??? ????? ??? ???? ???? ???? ????? ????.
    list_display = ('name', 'cafe', 'is_active')
    # ??? ??????? list_filter ??? ????? ??? ???? ???? ???? ????? ????.
    list_filter = ('is_active', 'cafe')
    # ??? ??????? search_fields ??? ????? ??? ???? ???? ???? ????? ????.
    search_fields = ('name',)

    # ???? ???? get_queryset ?????? ????? ?????? ?? ????? ????.
    def get_queryset(self, request):
        if request.user.is_superuser:
            return super().get_queryset(request).select_related('cafe')
        # ??? ??????? my_cafe ??? ????? ??? ???? ???? ???? ????? ????.
        my_cafe = get_owner_cafe(request.user)
        if not my_cafe:
            return super().get_queryset(request).none()
        return get_categories_for_cafe(my_cafe.id).select_related('cafe')

    # ???? ???? save_model ?????? ????? ?????? ?? ????? ????.
    def save_model(self, request, obj, form, change):
        if not request.user.is_superuser:
            # ??? ??????? my_cafe ??? ????? ??? ???? ???? ???? ????? ????.
            my_cafe = get_owner_cafe(request.user)
            if my_cafe:
                obj.cafe = my_cafe
        super().save_model(request, obj, form, change)


