# ???? ???? Command ???? ?????? ????????? ???? ???? ?????.
class Command(BaseCommand):
    # ??? ??????? help ??? ????? ??? ???? ???? ???? ????? ????.
    help = "Create cafe accounts and link them to cafes."

    # ???? ???? handle ?????? ????? ?????? ?? ????? ????.
    def handle(self, *args, **options):
        for entry in CAFES:
            # ??? ??????? name ??? ????? ??? ???? ???? ???? ????? ????.
            name = entry["name"]
            # ??? ??????? phone ??? ????? ??? ???? ???? ???? ????? ????.
            phone = entry["phone"]
            # ??? ??????? password ??? ????? ??? ???? ???? ???? ????? ????.
            password = entry["password"]
            # ??? ??????? email ??? ????? ??? ???? ???? ???? ????? ????.
            email = entry["email"]

            with transaction.atomic():
                # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
                user = User.objects.filter(phone_number=phone).first()
                if not user:
                    # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
                    user = User.objects.filter(email=email).first()

                if not user:
                    # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
                    user = User.objects.create_user(
                        # ??? ??????? email ??? ????? ??? ???? ???? ???? ????? ????.
                        email=email,
                        # ??? ??????? password ??? ????? ??? ???? ???? ???? ????? ????.
                        password=password,
                        # ??? ??????? full_name ??? ????? ??? ???? ???? ???? ????? ????.
                        full_name=name,
                        # ??? ??????? phone_number ??? ????? ??? ???? ???? ???? ????? ????.
                        phone_number=phone,
                        is_staff=True,
                    )
                    self.stdout.write(self.style.SUCCESS(f"Created user for {name}"))
                else:
                    # ??? ??????? updated ??? ????? ??? ???? ???? ???? ????? ????.
                    updated = False
                    if user.phone_number != phone:
                        user.phone_number = phone
                        # ??? ??????? updated ??? ????? ??? ???? ???? ???? ????? ????.
                        updated = True
                    if user.full_name != name:
                        user.full_name = name
                        # ??? ??????? updated ??? ????? ??? ???? ???? ???? ????? ????.
                        updated = True
                    if not user.check_password(password):
                        user.set_password(password)
                        # ??? ??????? updated ??? ????? ??? ???? ???? ???? ????? ????.
                        updated = True
                    if not user.is_staff:
                        user.is_staff = True
                        updated = True
                    if updated:
                        user.save()
                        self.stdout.write(self.style.WARNING(f"Updated user for {name}"))

                # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
                cafe = Cafe.objects.filter(owner=user).first()
                if cafe:
                    # ??? ??????? updates ??? ????? ??? ???? ???? ???? ????? ????.
                    updates = []
                    if cafe.name != name:
                        cafe.name = name
                        updates.append("name")
                    if not cafe.code:
                        cafe.code = _build_unique_cafe_code(name)
                        updates.append("code")
                    if not cafe.is_active:
                        cafe.is_active = True
                        updates.append("is_active")
                    if updates:
                        cafe.save(update_fields=updates)
                        self.stdout.write(self.style.WARNING(f"Updated cafe for {name}"))
                    initialize_cafe_runtime(cafe)
                    continue

                # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
                cafe = Cafe.objects.filter(name=name).first()
                if cafe:
                    updates = []
                    if cafe.owner is None:
                        cafe.owner = user
                        updates.append("owner")
                    if not cafe.code:
                        cafe.code = _build_unique_cafe_code(name)
                        updates.append("code")
                    if not cafe.is_active:
                        cafe.is_active = True
                        updates.append("is_active")
                    if updates:
                        cafe.save(update_fields=updates)
                        self.stdout.write(self.style.WARNING(f"Linked cafe {name} to user"))
                        initialize_cafe_runtime(cafe)
                    elif cafe.owner == user:
                        initialize_cafe_runtime(cafe)
                    else:
                        self.stdout.write(
                            self.style.WARNING(
                                f"Cafe {name} already linked to another user; no changes made."
                            )
                        )
                    continue

                # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
                cafe = provision_cafe(name=name, owner_id=user.id, is_active=True)
                self.stdout.write(self.style.SUCCESS(f"Created cafe {name}"))

        self.stdout.write(self.style.SUCCESS("Cafe seeding complete."))
