# ???? ???? get_super_admin_kpis ?????? ????? ?????? ?? ????? ????.
def get_super_admin_kpis():
    # ??? ??????? last_30_days ??? ????? ??? ???? ???? ???? ????? ????.
    last_30_days = timezone.now() - timedelta(days=30)
    # ??? ??????? sales ??? ????? ??? ???? ???? ???? ????? ????.
    sales = Order.objects.exclude(status=OrderStatus.CANCELLED).aggregate(
        # ??? ??????? total_sales ??? ????? ??? ???? ???? ???? ????? ????.
        total_sales=Sum("total_price"),
        # ??? ??????? total_orders ??? ????? ??? ???? ???? ???? ????? ????.
        total_orders=Count("id"),
    )
    return {
        "total_sales": sales["total_sales"] or 0,
        "total_orders": sales["total_orders"] or 0,
        "live_orders": Order.objects.filter(status__in=LIVE_ORDER_STATUSES).count(),
        "active_students": User.objects.filter(orders__created_at__gte=last_30_days).distinct().count(),
        "active_cafes": Cafe.objects.filter(is_active=True).count(),
    }


# ???? ???? get_super_admin_sales_series ?????? ????? ?????? ?? ????? ????.
def get_super_admin_sales_series(days: int = 7):
    # ??? ??????? today ??? ????? ??? ???? ???? ???? ????? ????.
    today = timezone.localdate()
    # ??? ??????? start_date ??? ????? ??? ???? ???? ???? ????? ????.
    start_date = today - timedelta(days=days - 1)
    # ??? ??????? rows ??? ????? ??? ???? ???? ???? ????? ????.
    rows = (
        Order.objects.exclude(status=OrderStatus.CANCELLED)
        .filter(created_at__date__gte=start_date)
        .annotate(day=TruncDate("created_at"))
        .values("day")
        .annotate(total=Sum("total_price"), orders=Count("id"))
        .order_by("day")
    )
    # ??? ??????? rows_by_day ??? ????? ??? ???? ???? ???? ????? ????.
    rows_by_day = {row["day"]: row for row in rows}
    # ??? ??????? series ??? ????? ??? ???? ???? ???? ????? ????.
    series = []
    for offset in range(days):
        # ??? ??????? day ??? ????? ??? ???? ???? ???? ????? ????.
        day = start_date + timedelta(days=offset)
        # ??? ??????? row ??? ????? ??? ???? ???? ???? ????? ????.
        row = rows_by_day.get(day)
        series.append(
            {
                "label": day.strftime("%d %b"),
                "sales": float(row["total"]) if row and row["total"] is not None else 0.0,
                "orders": row["orders"] if row else 0,
            }
        )
    return series


