# ???? ???? get_cafe_panel_snapshot ?????? ????? ?????? ?? ????? ????.
def get_cafe_panel_snapshot(cafe_id):
    # ??? ??????? orders ??? ????? ??? ???? ???? ???? ????? ????.
    orders = list(get_orders_for_cafe(cafe_id)[:40])
    # ??? ??????? grouped_orders ??? ????? ??? ???? ???? ???? ????? ????.
    grouped_orders = defaultdict(list)
    for status in KANBAN_STATUSES:
        grouped_orders[status] = []
    for order in orders:
        # ??? ??????? normalized_status ??? ????? ??? ???? ???? ???? ????? ????.
        normalized_status = OrderStatus.PREPARING if order.status == OrderStatus.ACCEPTED else order.status
        if normalized_status in grouped_orders:
            grouped_orders[normalized_status].append(order)

    # ??? ??????? products ??? ????? ??? ???? ???? ???? ????? ????.
    products = list(get_products_for_cafe(cafe_id)[:24])
    # ??? ??????? live_orders ??? ????? ??? ???? ???? ???? ????? ????.
    live_orders = len(grouped_orders[OrderStatus.PENDING]) + len(grouped_orders[OrderStatus.PREPARING]) + len(grouped_orders[OrderStatus.READY])
    # ??? ??????? low_stock_count ??? ????? ??? ???? ???? ???? ????? ????.
    low_stock_count = sum(1 for product in products if not product.is_in_stock)

    return {
        "orders_by_status": grouped_orders,
        "products": products,
        "kpis": {
            "live_orders": live_orders,
            "ready_orders": len(grouped_orders[OrderStatus.READY]),
            "today_sales": Order.objects.for_cafe(cafe_id)
            .exclude(status=OrderStatus.CANCELLED)
            .filter(created_at__date=timezone.localdate())
            .aggregate(total=Sum("total_price"))["total"]
            or 0,
            "out_of_stock": low_stock_count,
        },
    }


