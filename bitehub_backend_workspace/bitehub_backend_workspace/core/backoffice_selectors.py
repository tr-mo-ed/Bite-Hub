# ???? ???? get_all_cafes_for_admin ?????? ????? ?????? ?? ????? ????.
def get_all_cafes_for_admin():
    sales_subquery = (
        Order.objects.filter(cafe=OuterRef("pk"))
        .exclude(status=OrderStatus.CANCELLED)
        .values("cafe")
        .annotate(total=Sum("total_price"))
        .values("total")[:1]
    )
    orders_subquery = (
        Order.objects.filter(cafe=OuterRef("pk"))
        .values("cafe")
        .annotate(total=Count("id"))
        .values("total")[:1]
    )
    sold_items_subquery = (
        Order.objects.filter(cafe=OuterRef("pk"))
        .exclude(status=OrderStatus.CANCELLED)
        .values("cafe")
        .annotate(total=Sum("items__quantity"))
        .values("total")[:1]
    )
    return (
        Cafe.objects.select_related("faculty", "owner")
        .annotate(
            total_sales=Coalesce(
                Subquery(sales_subquery, output_field=DecimalField(max_digits=10, decimal_places=2)),
                Value(0),
                output_field=DecimalField(max_digits=10, decimal_places=2),
            ),
            total_orders=Coalesce(
                Subquery(orders_subquery, output_field=IntegerField()),
                Value(0),
                output_field=IntegerField(),
            ),
            sold_items=Coalesce(
                Subquery(sold_items_subquery, output_field=IntegerField()),
                Value(0),
                output_field=IntegerField(),
            ),
        )
        .order_by("name")
    )


# ???? ???? get_recent_error_snapshots ?????? ????? ?????? ?? ????? ????.
def get_recent_error_snapshots(limit: int = 10):
    # ??? ??????? entries ??? ????? ??? ???? ???? ???? ????? ????.
    entries = []
    # ??? ??????? sentry_dsn ??? ????? ??? ???? ???? ???? ????? ????.
    sentry_dsn = getattr(settings, "SENTRY_DSN", "")
    if sentry_dsn:
        entries.append(
            {
                "source": "Sentry",
                "level": "INFO",
                "message": "Sentry is configured. Remote exception capture is active.",
                "timestamp": timezone.now(),
            }
        )

    # ??? ??????? log_path ??? ????? ??? ???? ???? ???? ????? ????.
    log_path = Path(settings.BASE_DIR) / "logs" / "django.log"
    if log_path.exists():
        for raw_line in log_path.read_text(encoding="utf-8", errors="ignore").splitlines()[-200:]:
            if "ERROR" not in raw_line and "CRITICAL" not in raw_line:
                continue
            entries.append(
                {
                    "source": "django.log",
                    "level": "ERROR" if "ERROR" in raw_line else "CRITICAL",
                    "message": raw_line.strip()[-240:],
                    "timestamp": timezone.now(),
                }
            )
            if len(entries) >= limit:
                break

    return entries[:limit]


