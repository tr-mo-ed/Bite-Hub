@transaction.atomic
def toggle_cafe_active_status(*, cafe_id: int) -> Cafe:
    # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
    cafe = Cafe.objects.select_related("faculty", "owner").filter(pk=cafe_id).first()
    if cafe is None:
        raise ValidationServiceError("Cafe not found.")

    cafe.is_active = not cafe.is_active
    cafe.save(update_fields=["is_active", "updated_at"])
    return cafe


def _is_internal_cafe_operator(owner) -> bool:
    if owner is None:
        return False
    if getattr(owner, "is_superuser", False):
        return False
    return str(getattr(owner, "email", "")).lower().endswith("@bitehub.local")


@transaction.atomic
def delete_cafe_permanently(*, cafe_id: int) -> dict:
    cafe = Cafe.objects.select_related("owner").filter(pk=cafe_id).first()
    if cafe is None:
        raise ValidationServiceError("Cafe not found.")

    owner = cafe.owner
    cafe_name = cafe.name

    order_ids = list(Order.objects.filter(cafe_id=cafe.id).values_list("id", flat=True))
    product_ids = list(Product.objects.filter(cafe_id=cafe.id).values_list("id", flat=True))
    order_item_ids = set()
    if order_ids:
        order_item_ids.update(OrderItem.objects.filter(order_id__in=order_ids).values_list("id", flat=True))
    if product_ids:
        order_item_ids.update(OrderItem.objects.filter(product_id__in=product_ids).values_list("id", flat=True))
    deleted_order_items = OrderItem.objects.filter(id__in=order_item_ids).delete()[0] if order_item_ids else 0
    deleted_notifications = (
        Notification.objects.filter(order_id__in=order_ids).delete()[0] if order_ids else 0
    )
    deleted_orders = Order.objects.filter(id__in=order_ids).delete()[0] if order_ids else 0
    deleted_products = Product.objects.filter(id__in=product_ids).delete()[0] if product_ids else 0
    deleted_categories = Category.objects.filter(cafe_id=cafe.id).delete()[0]

    group = Group.objects.filter(name=CAFE_OWNER_GROUP_NAME).first()
    if owner is not None and group is not None:
        owner.groups.remove(group)

    cafe.delete()

    owner_deleted = False
    if _is_internal_cafe_operator(owner):
        owner.delete()
        owner_deleted = True
    elif owner is not None:
        remove_ineligible_wallet_if_empty(owner)

    cache.clear()
    return {
        "name": cafe_name,
        "orders": deleted_orders,
        "order_items": deleted_order_items,
        "notifications": deleted_notifications,
        "products": deleted_products,
        "categories": deleted_categories,
        "operator_deleted": owner_deleted,
    }
