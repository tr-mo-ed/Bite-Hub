# ???? ???? _build_unique_cafe_code ?????? ????? ?????? ?? ????? ????.
def _build_unique_cafe_code(name: str) -> str:
    # ??? ??????? base_code ??? ????? ??? ???? ???? ???? ????? ????.
    base_code = slugify(name) or "bite-hub-cafe"
    # ??? ??????? candidate ??? ????? ??? ???? ???? ???? ????? ????.
    candidate = base_code
    # ??? ??????? suffix ??? ????? ??? ???? ???? ???? ????? ????.
    suffix = 2
    while Cafe.objects.filter(code=candidate).exists():
        # ??? ??????? candidate ??? ????? ??? ???? ???? ???? ????? ????.
        candidate = f"{base_code}-{suffix}"
        suffix += 1
    return candidate


def _build_unique_faculty_code(name: str) -> str:
    base_code = slugify(name) or "faculty"
    candidate = base_code
    suffix = 2
    while Faculty.objects.filter(code=candidate).exists():
        candidate = f"{base_code}-{suffix}"
        suffix += 1
    return candidate


def _build_unique_manager_email(code: str) -> str:
    base = slugify(code) or "cafe"
    candidate = f"{base}@bitehub.local"
    suffix = 2
    while User.objects.filter(email__iexact=candidate).exists():
        candidate = f"{base}-{suffix}@bitehub.local"
        suffix += 1
    return candidate


def _build_unique_manager_phone() -> str:
    while True:
        candidate = f"09{secrets.randbelow(100_000_000):08d}"
        if not User.objects.filter(phone_number=candidate).exists():
            return candidate


# ???? ???? _get_existing_owner_cafe ?????? ????? ?????? ?? ????? ????.
def _get_existing_owner_cafe(owner):
    return getattr(owner, "my_cafe", None) if owner is not None else None


# ???? ???? _assert_owner_can_manage_cafe ?????? ????? ?????? ?? ????? ????.
def _assert_owner_can_manage_cafe(*, owner, cafe: Cafe | None = None) -> None:
    if owner is None:
        return

    # ??? ??????? existing_cafe ??? ????? ??? ???? ???? ???? ????? ????.
    existing_cafe = _get_existing_owner_cafe(owner)
    if existing_cafe is None:
        return

    if cafe is not None and existing_cafe.pk == cafe.pk:
        return

    raise ValidationServiceError("Selected manager is already assigned to another cafe.")


# ???? ???? _cleanup_cafe_owner_access ?????? ????? ?????? ?? ????? ????.
def _cleanup_cafe_owner_access(owner) -> None:
    if owner is None:
        return

    if _get_existing_owner_cafe(owner) is not None:
        return

    # ??? ??????? group ??? ????? ??? ???? ???? ???? ????? ????.
    group = Group.objects.filter(name=CAFE_OWNER_GROUP_NAME).first()
    if group is not None:
        owner.groups.remove(group)


# ???? ???? initialize_cafe_runtime ?????? ????? ?????? ?? ????? ????.
@transaction.atomic
def initialize_cafe_runtime(cafe: Cafe, *, previous_owner=None) -> Cafe:
    ensure_default_categories_for_cafe(cafe.id)

    # ??? ??????? owner ??? ????? ??? ???? ???? ???? ????? ????.
    owner = cafe.owner
    if previous_owner is not None and previous_owner != owner:
        _cleanup_cafe_owner_access(previous_owner)

    if owner is None:
        return cafe

    _assert_owner_can_manage_cafe(owner=owner, cafe=cafe)

    # ??? ??????? group ??? ????? ??? ???? ???? ???? ????? ????.
    group, _ = Group.objects.get_or_create(name=CAFE_OWNER_GROUP_NAME)
    if not owner.is_staff:
        owner.is_staff = True
        owner.save(update_fields=["is_staff"])
    owner.groups.add(group)
    remove_ineligible_wallet_if_empty(owner)

    return cafe


# ???? ???? provision_cafe ?????? ????? ?????? ?? ????? ????.
@transaction.atomic
def provision_cafe(
    *,
    name: str,
    code: str | None = None,
    faculty_id: int | None = None,
    owner_id: int | None = None,
    location: str = "",
    image=None,
    is_active: bool = True,
) -> Cafe:
    # ??? ??????? normalized_name ??? ????? ??? ???? ???? ???? ????? ????.
    normalized_name = (name or "").strip()
    if not normalized_name:
        raise ValidationServiceError("Cafe name is required.")

    # ??? ??????? normalized_code ??? ????? ??? ???? ???? ???? ????? ????.
    normalized_code = slugify(code or "")
    if not normalized_code:
        # ??? ??????? normalized_code ??? ????? ??? ???? ???? ???? ????? ????.
        normalized_code = _build_unique_cafe_code(normalized_name)
    elif Cafe.objects.filter(code=normalized_code).exists():
        raise ValidationServiceError("Cafe code is already in use.")

    # ??? ??????? faculty ??? ????? ??? ???? ???? ???? ????? ????.
    faculty = None
    if faculty_id:
        # ??? ??????? faculty ??? ????? ??? ???? ???? ???? ????? ????.
        faculty = Faculty.objects.filter(pk=faculty_id, is_active=True).first()
        if faculty is None:
            raise ValidationServiceError("Selected faculty was not found.")

    # ??? ??????? owner ??? ????? ??? ???? ???? ???? ????? ????.
    owner = None
    if owner_id:
        # ??? ??????? owner ??? ????? ??? ???? ???? ???? ????? ????.
        owner = User.objects.filter(pk=owner_id).first()
        if owner is None:
            raise ValidationServiceError("Selected manager was not found.")
        _assert_owner_can_manage_cafe(owner=owner)

    # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
    cafe = Cafe.objects.create(
        # ??? ??????? name ??? ????? ??? ???? ???? ???? ????? ????.
        name=normalized_name,
        # ??? ??????? code ??? ????? ??? ???? ???? ???? ????? ????.
        code=normalized_code,
        # ??? ??????? faculty ??? ????? ??? ???? ???? ???? ????? ????.
        faculty=faculty,
        # ??? ??????? owner ??? ????? ??? ???? ???? ???? ????? ????.
        owner=owner,
        # ??? ??????? location ??? ????? ??? ???? ???? ???? ????? ????.
        location=(location or "").strip(),
        # ??? ??????? image ??? ????? ??? ???? ???? ???? ????? ????.
        image=image,
        # ??? ??????? is_active ??? ????? ??? ???? ???? ???? ????? ????.
        is_active=is_active,
    )
    return initialize_cafe_runtime(cafe)


@transaction.atomic
def provision_cafe_with_credentials(
    *,
    faculty_name: str,
    password: str,
    name: str = "",
    code: str | None = None,
) -> Cafe:
    normalized_faculty_name = (faculty_name or "").strip()
    if not normalized_faculty_name:
        raise ValidationServiceError("College name is required.")

    normalized_password = (password or "").strip()
    if len(normalized_password) < 8:
        raise ValidationServiceError("Cafe password must be at least 8 characters.")

    faculty = Faculty.objects.filter(name__iexact=normalized_faculty_name).first()
    if faculty is None:
        faculty = Faculty.objects.create(
            name=normalized_faculty_name,
            code=_build_unique_faculty_code(normalized_faculty_name),
            is_active=True,
        )
    elif not faculty.is_active:
        faculty.is_active = True
        faculty.save(update_fields=["is_active", "updated_at"])

    cafe_name = (name or "").strip() or f"مقهى {faculty.name}"
    normalized_code = slugify(code or "") or _build_unique_cafe_code(cafe_name)
    if Cafe.objects.filter(code=normalized_code).exists():
        raise ValidationServiceError("Cafe code is already in use.")

    owner = User.objects.create_user(
        email=_build_unique_manager_email(normalized_code),
        password=normalized_password,
        full_name=cafe_name,
        phone_number=_build_unique_manager_phone(),
        is_staff=True,
    )

    cafe = Cafe.objects.create(
        name=cafe_name,
        code=normalized_code,
        faculty=faculty,
        owner=owner,
        is_active=True,
    )
    return initialize_cafe_runtime(cafe)


# ???? ???? toggle_product_stock ?????? ????? ?????? ?? ????? ????.
@transaction.atomic
def toggle_product_stock(*, cafe_id: int, product_id: int, is_available: bool, user) -> Product:
    # ??? ??????? product ??? ????? ??? ???? ???? ???? ????? ????.
    product = Product.objects.for_cafe(cafe_id).select_related("cafe").filter(pk=product_id).first()
    if product is None:
        raise ValidationServiceError("Product not found in the selected cafe.")

    if not getattr(user, "is_superuser", False):
        # ??? ??????? user_cafe ??? ????? ??? ???? ???? ???? ????? ????.
        user_cafe = getattr(user, "my_cafe", None)
        if user_cafe is None or user_cafe.id != product.cafe_id:
            raise ValidationServiceError("You cannot update stock outside your cafe scope.")

    if is_available and product.stock_quantity <= 0:
        raise ValidationServiceError("Cannot mark product available while stock is zero.")

    product.is_available = is_available
    product.save(update_fields=["is_available", "updated_at"])
    cache.clear()
    return product


@transaction.atomic
def save_cafe_product(*, cafe_id: int, user, data, files=None, product_id: int | None = None) -> Product:
    cafe = Cafe.objects.filter(pk=cafe_id, is_active=True).first()
    if cafe is None:
        raise ValidationServiceError("Cafe not found.")

    if not getattr(user, "is_superuser", False):
        user_cafe = getattr(user, "my_cafe", None)
        if user_cafe is None or user_cafe.id != cafe.id:
            raise ValidationServiceError("You cannot manage products outside your cafe scope.")

    product = None
    if product_id is not None:
        product = Product.objects.for_cafe(cafe.id).filter(pk=product_id).first()
        if product is None:
            raise ValidationServiceError("Product not found in the selected cafe.")

    mutable_data = data.copy()
    if "stock_quantity" not in mutable_data:
        mutable_data["stock_quantity"] = str(product.stock_quantity if product else 0)
    category_id = mutable_data.get("category")
    if not category_id:
        category = Category.objects.for_cafe(cafe.id).filter(is_active=True).order_by("name").first()
        if category is None:
            category = Category.objects.create(cafe=cafe, name="General")
        mutable_data["category"] = str(category.id)
    elif not Category.objects.for_cafe(cafe.id).filter(pk=category_id, is_active=True).exists():
        raise ValidationServiceError("Selected category does not belong to this cafe.")

    form = ProductForm(mutable_data, files or None, instance=product)
    form.fields["category"].queryset = Category.objects.for_cafe(cafe.id).filter(is_active=True)
    if not form.is_valid():
        first_error = next(iter(form.errors.values()))[0]
        raise ValidationServiceError(str(first_error))

    saved_product = form.save(commit=False)
    saved_product.cafe = cafe
    saved_product.save()
    form.save_m2m()
    cache.clear()
    return saved_product


