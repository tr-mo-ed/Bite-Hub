# ???? ???? get_request_user ?????? ????? ?????? ?? ????? ????.
def get_request_user(request):
    """
    Return the authenticated Django/DRF user.
    """
    if hasattr(request, 'user') and request.user and request.user.is_authenticated:
        return request.user
    raise ValueError("Authentication required")



# ???? ???? get_wallet ?????? ????? ?????? ?? ????? ????.
@api_view(['GET'])
@permission_classes([AllowAny])
def get_wallet(request):
    try:
        # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
        user = get_request_user(request)
    except Exception as e:
        return Response({'error': str(e)}, status=401)

    wallet = ensure_student_wallet(user)
    if wallet is None:
        return Response({'error': 'المحافظ متاحة لحسابات الطلاب فقط.'}, status=403)
    # ??? ??????? wallet ??? ????? ??? ???? ???? ???? ????? ????.
    wallet = _sync_wallet_balance(wallet)
    
    # جلب آخر 20 معاملة فقط لتقليل الضغط على السيرفر
    recent_transactions = wallet.transactions.order_by('-created_at')[:20]
    
    # نمرر المعاملات يدوياً للسيريالايزر
    serializer = WalletSerializer(wallet)
    # ??? ??????? data ??? ????? ??? ???? ???? ???? ????? ????.
    data = serializer.data
    # نستبدل المعاملات بالقائمة المحدثة (لضمان الترتيب والعدد)
    from .serializers import TransactionSerializer
    data['transactions'] = TransactionSerializer(recent_transactions, many=True).data
    
    return Response(data)


# ???? ???? transfer_wallet ?????? ????? ?????? ?? ????? ????.
@api_view(['POST'])
@permission_classes([AllowAny])
def transfer_wallet(request):
    try:
        # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
        user = get_request_user(request)
    except Exception as e:
        return Response({'error': str(e)}, status=401)

    # ??? ??????? wallet_code ??? ????? ??? ???? ???? ???? ????? ????.
    wallet_code = (request.data.get('wallet_code')
                   or request.data.get('link_code')
                   or '')
    # ??? ??????? amount_raw ??? ????? ??? ???? ???? ???? ????? ????.
    amount_raw = request.data.get('amount')
    # ??? ??????? note ??? ????? ??? ???? ???? ???? ????? ????.
    note = (request.data.get('note') or request.data.get('description') or '').strip()
    recipient_name = (request.data.get('recipient_name') or '').strip()

    if not wallet_code:
        return Response({'error': 'يجب إدخال رقم المحفظة.'}, status=400)

    try:
        # ??? ??????? amount ??? ????? ??? ???? ???? ???? ????? ????.
        amount = Decimal(str(amount_raw))
    except Exception:
        return Response({'error': 'قيمة غير صحيحة.'}, status=400)

    if amount <= 0:
        return Response({'error': 'يجب أن تكون القيمة أكبر من صفر.'}, status=400)

    try:
        # ??? ??????? sender_wallet ??? ????? ??? ???? ???? ???? ????? ????.
        sender_wallet_lookup = Wallet.objects.get(user=user, user__is_staff=False, user__is_superuser=False, user__my_cafe__isnull=True)

        # ??? ??????? target_wallet ??? ????? ??? ???? ???? ???? ????? ????.
        target_wallet_lookup = Wallet.objects.filter(
            link_code__iexact=wallet_code,
            user__is_staff=False,
            user__is_superuser=False,
            user__my_cafe__isnull=True,
        ).first()

        if not target_wallet_lookup:
            return Response({'error': 'المحفظة المستلمة غير موجودة.'}, status=404)

        if sender_wallet_lookup.id == target_wallet_lookup.id:
            return Response({'error': 'لا يمكن التحويل إلى نفس المحفظة.'}, status=400)

        with transaction.atomic():
            wallet_ids = sorted([sender_wallet_lookup.id, target_wallet_lookup.id])
            locked_wallets = {
                wallet.id: wallet
                for wallet in Wallet.objects.select_for_update()
                .select_related("user")
                .filter(id__in=wallet_ids)
            }
            sender_wallet = locked_wallets[sender_wallet_lookup.id]
            target_wallet = locked_wallets[target_wallet_lookup.id]

            if sender_wallet.balance < amount:
                return Response({"error": "Insufficient wallet balance."}, status=400)
            # ??? ??????? sender_desc ??? ????? ??? ???? ???? ???? ????? ????.
            recipient_label = recipient_name or target_wallet.user.full_name
            sender_desc = f"تحويل إلى {recipient_label}"
            # ??? ??????? receiver_desc ??? ????? ??? ???? ???? ???? ????? ????.
            receiver_desc = f"تحويل من {user.full_name}"
            if note:
                # ??? ??????? sender_desc ??? ????? ??? ???? ???? ???? ????? ????.
                sender_desc = f"{sender_desc} - {note}"
                # ??? ??????? receiver_desc ??? ????? ??? ???? ???? ???? ????? ????.
                receiver_desc = f"{receiver_desc} - {note}"

            Transaction.objects.create(
                # ??? ??????? wallet ??? ????? ??? ???? ???? ???? ????? ????.
                wallet=sender_wallet,
                # ??? ??????? amount ??? ????? ??? ???? ???? ???? ????? ????.
                amount=amount,
                # ??? ??????? transaction_type ??? ????? ??? ???? ???? ???? ????? ????.
                transaction_type='WITHDRAWAL',
                # ??? ??????? source ??? ????? ??? ???? ???? ???? ????? ????.
                source='USER',
                # ??? ??????? description ??? ????? ??? ???? ???? ???? ????? ????.
                description=sender_desc,
            )
            Transaction.objects.create(
                # ??? ??????? wallet ??? ????? ??? ???? ???? ???? ????? ????.
                wallet=target_wallet,
                # ??? ??????? amount ??? ????? ??? ???? ???? ???? ????? ????.
                amount=amount,
                # ??? ??????? transaction_type ??? ????? ??? ???? ???? ???? ????? ????.
                transaction_type='DEPOSIT',
                # ??? ??????? source ??? ????? ??? ???? ???? ???? ????? ????.
                source='USER',
                # ??? ??????? description ??? ????? ??? ???? ???? ???? ????? ????.
                description=receiver_desc,
            )

            send_real_notification(
                sender_wallet.user,
                "تم إرسال التحويل",
                f"تم تحويل {amount} د.ل إلى {recipient_label}.",
                event_type="WALLET_TRANSFER_SENT",
            )
            send_real_notification(
                target_wallet.user,
                "وصل تحويل إلى محفظتك",
                f"استلمت {amount} د.ل من {user.full_name}.",
                event_type="WALLET_TRANSFER_RECEIVED",
            )

        sender_wallet.refresh_from_db()
        return Response({'success': True, 'balance': sender_wallet.balance})
    except Wallet.DoesNotExist:
        return Response({'error': 'المحفظة غير موجودة.'}, status=404)
    except ValueError as e:
        return Response({'error': str(e)}, status=400)
    except Exception as e:
        return Response({'error': str(e)}, status=500)


# ???? ???? link_wallet ?????? ????? ?????? ?? ????? ????.
@api_view(['POST'])
@permission_classes([AllowAny])
def link_wallet(request):
    try:
        # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
        user = get_request_user(request)
    except Exception as e:
        return Response({'error': str(e)}, status=401)

    # ??? ??????? link_code ??? ????? ??? ???? ???? ???? ????? ????.
    link_code = request.data.get('link_code')
    if not link_code:
        return Response({'error': 'Code is required'}, status=400)
    
    try:
        # التأكد من أن الكود فريد وغير مستخدم من شخص آخر
        if Wallet.objects.filter(link_code=link_code).exclude(user=user).exists():
             return Response({'error': 'هذا الكود مستخدم بالفعل من قبل طالب آخر'}, status=400)
        
        wallet = ensure_student_wallet(user)
        if wallet is None:
            return Response({'error': 'المحافظ متاحة لحسابات الطلاب فقط.'}, status=403)
        wallet.link_code = link_code
        wallet.save()
        return Response({'success': True, 'message': 'تم ربط المحفظة بنجاح'})
    except Exception as e:
         return Response({'error': str(e)}, status=500)


@api_view(['POST'])
@permission_classes([AllowAny])
def link_nfc_card(request):
    try:
        user = get_request_user(request)
    except Exception as exc:
        return Response({'error': str(exc)}, status=401)

    card_uid = (request.data.get('card_uid') or '').strip().upper()
    if not card_uid or len(card_uid) > 20:
        return Response({'error': 'معرف بطاقة NFC غير صحيح.'}, status=400)

    wallet = ensure_student_wallet(user)
    if wallet is None:
        return Response({'error': 'المحافظ متاحة لحسابات الطلاب فقط.'}, status=403)
    if Wallet.objects.filter(nfc_card_uid__iexact=card_uid).exclude(pk=wallet.pk).exists():
        return Response({'error': 'هذه البطاقة مرتبطة بطالب آخر.'}, status=400)

    wallet.nfc_card_uid = card_uid
    wallet.save(update_fields=['nfc_card_uid', 'updated_at'])
    return Response({
        'success': True,
        'message': 'تم ربط بطاقة NFC بالمحفظة.',
        'has_nfc_card': True,
        'nfc_card_last4': card_uid[-4:],
    })


