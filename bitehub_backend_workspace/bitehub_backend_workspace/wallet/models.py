INTERNAL_CAFE_EMAIL_DOMAIN = "@bitehub.local"

# ???? ???? Wallet ???? ?????? ????????? ???? ???? ?????.
class Wallet(models.Model):
    # ??? ??????? user ?????? ??? ?????? ???? ??? ??????? ????.
    user = models.OneToOneField(settings.AUTH_USER_MODEL, on_delete=models.CASCADE, related_name='wallet', verbose_name="الطالب")
    # ??? ??????? balance ?????? ??? ?????? ???? ??? ??????? ????.
    balance = models.DecimalField(max_digits=10, decimal_places=2, default=0.00, verbose_name="الرصيد الحالي")
    # ??? ??????? link_code ?????? ??? ?????? ???? ??? ??????? ????.
    link_code = models.CharField(max_length=20, unique=True, blank=True, null=True, verbose_name="كود المحفظة")
    nfc_card_uid = models.CharField(
        max_length=20,
        unique=True,
        blank=True,
        null=True,
        verbose_name="معرف بطاقة NFC",
    )
    # يمكنك جعل الكلية اختيار من قائمة إذا أردت لاحقاً
    college = models.CharField(max_length=100, default="Bite Hub", verbose_name="الكلية")
    # ??? ??????? updated_at ?????? ??? ?????? ???? ??? ??????? ????.
    updated_at = models.DateTimeField(auto_now=True, verbose_name="آخر تحديث")

    # ???? ???? save ?????? ????? ?????? ?? ????? ????.
    def save(self, *args, **kwargs):
        if not self.link_code:
            self.link_code = str(uuid.uuid4())[:8].upper()
        super().save(*args, **kwargs)

    # ???? ???? __str__ ?????? ????? ?????? ?? ????? ????.
    def __str__(self):
        return f"محفظة: {self.user} | {self.balance}"


def user_can_have_wallet(user) -> bool:
    if user is None or not getattr(user, "pk", None):
        return False
    email = str(getattr(user, "email", "") or "").strip().lower()
    if (
        getattr(user, "is_superuser", False)
        or getattr(user, "is_staff", False)
        or email.endswith(INTERNAL_CAFE_EMAIL_DOMAIN)
    ):
        return False
    try:
        user.my_cafe
    except ObjectDoesNotExist:
        return True
    return False


def ineligible_wallets_queryset(queryset=None):
    base_queryset = queryset if queryset is not None else Wallet.objects.all()
    return base_queryset.filter(
        Q(user__is_superuser=True)
        | Q(user__is_staff=True)
        | Q(user__email__iendswith=INTERNAL_CAFE_EMAIL_DOMAIN)
        | Q(user__my_cafe__isnull=False)
    ).distinct()


def student_wallets_queryset(queryset=None):
    base_queryset = queryset if queryset is not None else Wallet.objects.all()
    return (
        base_queryset.select_related("user")
        .exclude(pk__in=ineligible_wallets_queryset(base_queryset).values("pk"))
        .order_by("user__full_name", "user__email", "id")
    )


def purge_ineligible_wallets() -> int:
    wallets = list(ineligible_wallets_queryset().values_list("pk", flat=True))
    if not wallets:
        return 0
    deleted_count, _ = Wallet.objects.filter(pk__in=wallets).delete()
    return deleted_count


def remove_ineligible_wallet_if_empty(user) -> None:
    if user_can_have_wallet(user):
        return
    wallet = Wallet.objects.filter(user=user).first()
    if wallet is not None and not wallet.transactions.exists():
        wallet.delete()


def ensure_student_wallet(user):
    if not user_can_have_wallet(user):
        remove_ineligible_wallet_if_empty(user)
        return None
    wallet, _ = Wallet.objects.get_or_create(user=user)
    return wallet


# ???? ???? Transaction ???? ?????? ????????? ???? ???? ?????.
class Transaction(models.Model):
    # ??? ??????? TRANSACTION_TYPES ??? ????? ??? ???? ???? ???? ????? ????.
    TRANSACTION_TYPES = (
        ('DEPOSIT', 'شحن'),
        ('WITHDRAWAL', 'خصم (دفع)'),
    )
    # ??? ??????? SOURCES ??? ????? ??? ???? ???? ???? ????? ????.
    SOURCES = (
        ('SYSTEM', 'المنظومة'), 
        ('APP', 'التطبيق'),
        ('USER', 'المستخدم'),
        ('NFC', 'بطاقة NFC'),
    )

    # ??? ??????? id ?????? ??? ?????? ???? ??? ??????? ????.
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    # ??? ??????? wallet ?????? ??? ?????? ???? ??? ??????? ????.
    wallet = models.ForeignKey(Wallet, on_delete=models.CASCADE, related_name='transactions')
    # ??? ??????? amount ?????? ??? ?????? ???? ??? ??????? ????.
    amount = models.DecimalField(max_digits=10, decimal_places=2, verbose_name="القيمة")
    # ??? ??????? transaction_type ?????? ??? ?????? ???? ??? ??????? ????.
    transaction_type = models.CharField(max_length=20, choices=TRANSACTION_TYPES)
    # ??? ??????? source ?????? ??? ?????? ???? ??? ??????? ????.
    source = models.CharField(max_length=20, choices=SOURCES, default='SYSTEM')
    # ??? ??????? description ?????? ??? ?????? ???? ??? ??????? ????.
    description = models.TextField(blank=True, null=True)
    # ??? ??????? created_at ?????? ??? ?????? ???? ??? ??????? ????.
    created_at = models.DateTimeField(auto_now_add=True)

    # ???? ???? save ?????? ????? ?????? ?? ????? ????.
    def save(self, *args, **kwargs):
        # Transaction Logic: Update wallet balance atomically
        with transaction.atomic():
            if self._state.adding:
                # Lock wallet row for update to prevent race conditions
                wallet = Wallet.objects.select_for_update().get(pk=self.wallet.pk)
                
                if self.transaction_type == 'DEPOSIT':
                    wallet.balance += self.amount
                elif self.transaction_type == 'WITHDRAWAL':
                    if wallet.balance >= self.amount:
                        wallet.balance -= self.amount
                    else:
                        raise ValueError("الرصيد لا يكفي لإتمام العملية!")
                
                wallet.save()
            
            super().save(*args, **kwargs)

