from __future__ import annotations

from django.db.models import Count, Prefetch, Q, QuerySet

from .models import Cafe, Category, Order, OrderItem, Product


# ???? ???? get_active_cafes ?????? ????? ?????? ?? ????? ????.
def get_active_cafes() -> QuerySet[Cafe]:
    return Cafe.objects.active().order_by("name")


# ???? ???? get_cafe_for_user ?????? ????? ?????? ?? ????? ????.
def get_cafe_for_user(user) -> Cafe | None:
    # ??? ??????? cafe ??? ????? ??? ???? ???? ???? ????? ????.
    cafe = getattr(user, "my_cafe", None)
    if cafe:
        return cafe
    if getattr(user, "is_superuser", False):
        return get_active_cafes().first()
    if getattr(user, "is_staff", False):
        # ??? ??????? active_cafes ??? ????? ??? ???? ???? ???? ????? ????.
        active_cafes = get_active_cafes()
        if active_cafes.count() == 1:
            return active_cafes.first()
    return None


# ???? ???? get_products_for_cafe ?????? ????? ?????? ?? ????? ????.
def get_products_for_cafe(cafe_id) -> QuerySet[Product]:
    return (
        Product.objects.for_cafe(cafe_id)
        .select_related("cafe", "category")
        .order_by("-created_at")
    )


# ???? ???? get_active_products_for_cafe ?????? ????? ?????? ?? ????? ????.
def get_active_products_for_cafe(cafe_id) -> QuerySet[Product]:
    return get_products_for_cafe(cafe_id).filter(is_available=True, stock_quantity__gt=0)


# ???? ???? get_categories_for_cafe ?????? ????? ?????? ?? ????? ????.
def get_categories_for_cafe(cafe_id) -> QuerySet[Category]:
    return (
        Category.objects.for_cafe(cafe_id)
        .select_related("cafe")
        .filter(is_active=True)
        .order_by("name")
    )


# ???? ???? get_order_details_for_cafe ?????? ????? ?????? ?? ????? ????.
def get_order_details_for_cafe(order_id, cafe_id) -> Order:
    # ??? ??????? order_items_prefetch ??? ????? ??? ???? ???? ???? ????? ????.
    order_items_prefetch = Prefetch(
        "items",
        # ??? ??????? queryset ??? ????? ??? ???? ???? ???? ????? ????.
        queryset=OrderItem.objects.select_related("product"),
    )
    return (
        Order.objects.for_cafe(cafe_id)
        .select_related("cafe", "user")
        .prefetch_related(order_items_prefetch)
        .get(pk=order_id)
    )


