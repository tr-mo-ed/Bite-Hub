# ???? ???? get_user_orders ?????? ????? ?????? ?? ????? ????.
def get_user_orders(user) -> QuerySet[Order]:
    # ??? ??????? order_items_prefetch ??? ????? ??? ???? ???? ???? ????? ????.
    order_items_prefetch = Prefetch(
        "items",
        # ??? ??????? queryset ??? ????? ??? ???? ???? ???? ????? ????.
        queryset=OrderItem.objects.select_related("product"),
    )
    return (
        Order.objects.filter(user=user)
        .select_related("cafe")
        .prefetch_related(order_items_prefetch)
        .order_by("-created_at")
    )
# ???? ???? get_customers_for_cafe ?????? ????? ?????? ?? ????? ????.
def get_customers_for_cafe(cafe_id):
    from users.models import User

    return (
        User.objects.filter(orders__cafe_id=cafe_id)
        .distinct()
        .annotate(orders_count=Count("orders", filter=Q(orders__cafe_id=cafe_id)))
        .order_by("-date_joined")
    )
