def _repair_text(value):
    if not isinstance(value, str):
        return value
    if not any(marker in value for marker in ("Ø", "Ù", "Ã", "ð")):
        return value
    try:
        return value.encode("latin1").decode("utf-8")
    except UnicodeError:
        return value


def _repair_payload(value):
    if isinstance(value, dict):
        return {key: _repair_payload(item) for key, item in value.items()}
    if isinstance(value, list):
        return [_repair_payload(item) for item in value]
    return _repair_text(value)


class CleanTextSerializerMixin:
    def to_representation(self, instance):
        return _repair_payload(super().to_representation(instance))


# ???? ???? _build_image_url ?????? ????? ?????? ?? ????? ????.
def _build_image_url(image_value, request=None):
    if not image_value:
        return None

    # ??? ??????? image_str ??? ????? ??? ???? ???? ???? ????? ????.
    image_str = str(image_value)
    if image_str.startswith('http'):
        return image_str

    if request:
        return request.build_absolute_uri(image_value.url if hasattr(image_value, 'url') else image_str)

    return f"/media/{image_str}" if not image_str.startswith('/media/') else image_str


