# ???? ???? _generate_order_number ?????? ????? ?????? ?? ????? ????.
def _generate_order_number() -> str:
    while True:
        candidate = f"BH-{secrets.randbelow(1_000_000):06d}"
        if not Order.objects.filter(order_number=candidate).exists():
            return candidate


# ???? ???? _broadcast_order_event ?????? ????? ?????? ?? ????? ????.
def _broadcast_order_event(order: Order, event_name: str) -> None:
    # ??? ??????? channel_layer ??? ????? ??? ???? ???? ???? ????? ????.
    channel_layer = get_channel_layer()
    if channel_layer is None:
        return

    # ??? ??????? items_payload ??? ????? ??? ???? ???? ???? ????? ????.
    items_payload = [
        {
            "product_id": item.product_id,
            "product_name": item.product.name,
            "quantity": item.quantity,
            "price": str(item.price),
            "options": item.options,
        }
        for item in order.items.select_related("product").all()
    ]

    # ??? ??????? payload ??? ????? ??? ???? ???? ???? ????? ????.
    payload = {
        "id": order.id,
        "order_number": order.display_order_number,
        "cafe_id": order.cafe_id,
        "cafe_name": order.cafe.name if getattr(order, "cafe", None) else "",
        "user_id": order.user_id,
        "total_price": str(order.total_price),
        "status": order.status,
        "payment_method": order.payment_method,
        "notes": order.notes,
        "created_at": order.created_at.isoformat() if order.created_at else None,
        "updated_at": order.updated_at.isoformat() if order.updated_at else None,
        "items": items_payload,
    }

    try:
        async_to_sync(channel_layer.group_send)(
            f"cafe_orders_{order.cafe_id}",
            {
                "type": "order_event",
                "event": event_name,
                "payload": payload,
            },
        )
    except Exception:
        logger.exception("Failed to broadcast order event %s for order %s.", event_name, order.pk)


def _normalize_payment_method(payment_method: str | None) -> str:
    normalized_payment_method = (payment_method or PaymentMethod.WALLET).strip().upper()
    if normalized_payment_method not in {
        PaymentMethod.WALLET,
        PaymentMethod.CASH,
        PaymentMethod.NFC,
    }:
        raise ValidationServiceError("Invalid payment method.")
    return normalized_payment_method


# ???? ???? create_category_for_cafe ?????? ????? ?????? ?? ????? ????.
def create_category_for_cafe(cafe_id, name, *, is_active: bool = True) -> Category:
    # ??? ??????? normalized_name ??? ????? ??? ???? ???? ???? ????? ????.
    normalized_name = (name or "").strip()
    if not normalized_name:
        raise ValidationServiceError("Category name is required.")

    # ??? ??????? existing ??? ????? ??? ???? ???? ???? ????? ????.
    existing = Category.objects.for_cafe(cafe_id).filter(name__iexact=normalized_name).first()
    if existing:
        if existing.is_active != is_active:
            existing.is_active = is_active
            existing.save(update_fields=["is_active", "updated_at"])
        return existing

    return Category.objects.create(
        # ??? ??????? cafe_id ??? ????? ??? ???? ???? ???? ????? ????.
        cafe_id=cafe_id,
        # ??? ??????? name ??? ????? ??? ???? ???? ???? ????? ????.
        name=normalized_name,
        # ??? ??????? is_active ??? ????? ??? ???? ???? ???? ????? ????.
        is_active=is_active,
    )


# ???? ???? ensure_default_categories_for_cafe ?????? ????? ?????? ?? ????? ????.
def ensure_default_categories_for_cafe(cafe_id):
    for name in DEFAULT_CATEGORY_NAMES:
        create_category_for_cafe(cafe_id, name)
    return get_categories_for_cafe(cafe_id)


# ???? ???? create_order ?????? ????? ?????? ?? ????? ????.
def create_order(
    user,
    cafe_id,
    items_data,
    *,
    total_price: Decimal | None = None,
    payment_method: str = PaymentMethod.WALLET,
    nfc_card_uid: str | None = None,
    order_note: str | None = None,
) -> Order:
    if not items_data:
        raise ValidationServiceError("Order items are required.")

    payment_method = _normalize_payment_method(payment_method)
    normalized_order_note = (order_note or "").strip()
    if len(normalized_order_note) > 500:
        raise ValidationServiceError("Order note must not exceed 500 characters.")

    # ??? ??????? raw_product_ids ??? ????? ??? ???? ???? ???? ????? ????.
    raw_product_ids = [item.get("product_id") for item in items_data]
    # ??? ??????? product_ids ??? ????? ??? ???? ???? ???? ????? ????.
    product_ids = [product_id for product_id in raw_product_ids if product_id is not None]
    if not product_ids:
        raise ValidationServiceError("Order items must include product_id.")

    with transaction.atomic():
        # ??? ??????? normalized_cafe_id ??? ????? ??? ???? ???? ???? ????? ????.
        normalized_cafe_id = str(cafe_id).strip() if cafe_id is not None else ""
        if normalized_cafe_id:
            # ??? ??????? source_queryset ??? ????? ??? ???? ???? ???? ????? ????.
            source_queryset = (
                Product.objects.select_for_update()
                .for_cafe(normalized_cafe_id)
                .select_related("cafe")
                .filter(id__in=product_ids)
            )
        else:
            # ??? ??????? source_queryset ??? ????? ??? ???? ???? ???? ????? ????.
            source_queryset = (
                Product.objects.select_for_update()
                .select_related("cafe")
                .filter(id__in=product_ids)
            )

        # ??? ??????? products ??? ????? ??? ???? ???? ???? ????? ????.
        products = {str(product.id): product for product in source_queryset}

        if len(products) != len(set(map(str, product_ids))):
            raise ValidationServiceError("Some products do not belong to the selected cafe.")

        if not normalized_cafe_id:
            # ??? ??????? cafe_ids ??? ????? ??? ???? ???? ???? ????? ????.
            cafe_ids = {str(product.cafe_id) for product in products.values()}
            if len(cafe_ids) != 1:
                raise ValidationServiceError("Order items must belong to a single cafe.")
            # ??? ??????? normalized_cafe_id ??? ????? ??? ???? ???? ???? ????? ????.
            normalized_cafe_id = cafe_ids.pop()

        cafes_by_id = {str(product.cafe_id): product.cafe for product in products.values()}
        selected_cafe = cafes_by_id.get(str(normalized_cafe_id))
        if selected_cafe is None:
            raise ValidationServiceError("Selected cafe was not found.")
        if not selected_cafe.is_active:
            raise ValidationServiceError("Cafe is not active.")
        if not selected_cafe.is_accepting_orders:
            raise ValidationServiceError("Cafe is currently closed for orders.")

        # ??? ??????? computed_total ??? ????? ??? ???? ???? ???? ????? ????.
        computed_total = Decimal("0.00")
        # ??? ??????? order_items ??? ????? ??? ???? ???? ???? ????? ????.
        order_items = []
        requested_quantities = {}

        for item in items_data:
            # ??? ??????? product_id ??? ????? ??? ???? ???? ???? ????? ????.
            product_id = str(item.get("product_id"))
            # ??? ??????? product ??? ????? ??? ???? ???? ???? ????? ????.
            product = products.get(product_id)
            if product is None:
                raise ValidationServiceError("Invalid product in order items.")

            # ??? ??????? quantity ??? ????? ??? ???? ???? ???? ????? ????.
            quantity = int(item.get("quantity", item.get("qty", 1)) or 1)
            if quantity <= 0:
                raise ValidationServiceError("Quantity must be greater than zero.")

            requested_quantities[product.id] = requested_quantities.get(product.id, 0) + quantity
            if not product.is_in_stock:
                raise ValidationServiceError(f"{product.name} is out of stock.")
            if requested_quantities[product.id] > product.stock_quantity:
                raise ValidationServiceError(
                    f"Only {product.stock_quantity} items left for {product.name}."
                )

            # ??? ??????? options ??? ????? ??? ???? ???? ???? ????? ????.
            options = (item.get("options") or item.get("note") or "").strip()
            # ??? ??????? line_total ??? ????? ??? ???? ???? ???? ????? ????.
            line_total = product.price * quantity
            computed_total += line_total
            order_items.append(
                OrderItem(
                    # ??? ??????? product ??? ????? ??? ???? ???? ???? ????? ????.
                    product=product,
                    # ??? ??????? quantity ??? ????? ??? ???? ???? ???? ????? ????.
                    quantity=quantity,
                    # ??? ??????? price ??? ????? ??? ???? ???? ???? ????? ????.
                    price=product.price,
                    # ??? ??????? options ??? ????? ??? ???? ???? ???? ????? ????.
                    options=options,
                )
            )

        if total_price is not None:
            # ??? ??????? normalized_total ??? ????? ??? ???? ???? ???? ????? ????.
            normalized_total = Decimal(str(total_price))
            if normalized_total != computed_total:
                raise ValidationServiceError("Submitted total_price does not match server calculation.")

        if payment_method in {PaymentMethod.WALLET, PaymentMethod.NFC}:
            try:
                # ??? ??????? wallet ??? ????? ??? ???? ???? ???? ????? ????.
                wallet = Wallet.objects.select_for_update().get(user=user)
            except Wallet.DoesNotExist as exc:
                raise NotFoundServiceError("Wallet not found.") from exc

            if wallet.balance < computed_total:
                raise ValidationServiceError("Insufficient wallet balance.")

            transaction_source = "APP"
            transaction_description = "Order payment"
            if payment_method == PaymentMethod.NFC:
                normalized_card_uid = (nfc_card_uid or "").strip().upper()
                if not normalized_card_uid:
                    raise ValidationServiceError("NFC card is required.")
                if not wallet.nfc_card_uid:
                    raise ValidationServiceError("Link an NFC card to your wallet first.")
                if wallet.nfc_card_uid.upper() != normalized_card_uid:
                    raise ValidationServiceError("This NFC card does not belong to your wallet.")
                transaction_source = "NFC"
                transaction_description = "Order payment by NFC card"

            Transaction.objects.create(
                # ??? ??????? wallet ??? ????? ??? ???? ???? ???? ????? ????.
                wallet=wallet,
                cafe_id=normalized_cafe_id,
                # ??? ??????? amount ??? ????? ??? ???? ???? ???? ????? ????.
                amount=computed_total,
                # ??? ??????? transaction_type ??? ????? ??? ???? ???? ???? ????? ????.
                transaction_type="WITHDRAWAL",
                # ??? ??????? source ??? ????? ??? ???? ???? ???? ????? ????.
                source=transaction_source,
                # ??? ??????? description ??? ????? ??? ???? ???? ???? ????? ????.
                description=transaction_description,
            )

        for product_id, quantity in requested_quantities.items():
            product = products[str(product_id)]
            product.stock_quantity -= quantity
            update_fields = ["stock_quantity", "updated_at"]
            if product.stock_quantity <= 0:
                product.is_available = False
                update_fields.append("is_available")
            product.save(update_fields=update_fields)

        # ??? ??????? order ??? ????? ??? ???? ???? ???? ????? ????.
        order = Order.objects.create(
            # ??? ??????? user ??? ????? ??? ???? ???? ???? ????? ????.
            user=user,
            # ??? ??????? cafe_id ??? ????? ??? ???? ???? ???? ????? ????.
            cafe_id=normalized_cafe_id,
            # ??? ??????? total_price ??? ????? ??? ???? ???? ???? ????? ????.
            total_price=computed_total,
            # ??? ??????? status ??? ????? ??? ???? ???? ???? ????? ????.
            status="PENDING",
            # ??? ??????? payment_method ??? ????? ??? ???? ???? ???? ????? ????.
            payment_method=payment_method,
            # ??? ??????? order_number ??? ????? ??? ???? ???? ???? ????? ????.
            order_number=_generate_order_number(),
            notes=normalized_order_note,
        )

        for order_item in order_items:
            order_item.order = order

        OrderItem.objects.bulk_create(order_items)

    try:
        if payment_method in {PaymentMethod.WALLET, PaymentMethod.NFC}:
            send_real_notification(
                user,
                "تم خصم قيمة الطلب",
                f"تم خصم {computed_total} د.ل من محفظتك للطلب #{order.display_order_number}.",
                event_type="WALLET_ORDER_WITHDRAWAL",
                order=order,
            )
        send_real_notification(
            user,
            "تم استلام طلبك",
            f"طلبك #{order.display_order_number} قيد المراجعة.",
            event_type="ORDER_CREATED",
            order=order,
        )
    except Exception:
        pass

    _broadcast_order_event(order, "order.created")

    return order


# // هني درنا إرجاع للفلوس لمحفظة الطالب بعد ما ألغى الطلب باش ما يضيعش حقه.
def _refund_wallet_for_cancelled_order(order: Order) -> None:
    try:
        wallet = Wallet.objects.select_for_update().get(user=order.user)
    except Wallet.DoesNotExist as exc:
        raise NotFoundServiceError("Wallet not found.") from exc

    Transaction.objects.create(
        wallet=wallet,
        cafe=order.cafe,
        amount=order.total_price,
        transaction_type="DEPOSIT",
        source="SYSTEM",
        description=f"Refund for cancelled order #{order.display_order_number}",
    )


# ???? ???? cancel_user_order ?????? ????? ?????? ?? ????? ????.
def cancel_user_order(order_id: int, user) -> Order:
    with transaction.atomic():
        try:
            # ??? ??????? order ??? ????? ??? ???? ???? ???? ????? ????.
            order = (
                Order.objects.select_for_update()
                .select_related("cafe", "user")
                .get(pk=order_id, user=user)
            )
        except Order.DoesNotExist as exc:
            raise NotFoundServiceError("Order not found.") from exc

        # ??? ??????? current_status ??? ????? ??? ???? ???? ???? ????? ????.
        current_status = (order.status or "").strip().upper()
        if current_status not in {"PENDING", "NEW"}:
            raise ValidationServiceError("Only pending orders can be cancelled.")

        order.status = "CANCELLED"
        order.save(update_fields=["status", "updated_at"])

        if _normalize_payment_method(order.payment_method) in {
            PaymentMethod.WALLET,
            PaymentMethod.NFC,
        }:
            _refund_wallet_for_cancelled_order(order)

    send_real_notification(
        order.user,
        "تم إلغاء الطلب",
        f"تم إلغاء طلبك #{order.display_order_number}.",
        event_type="ORDER_CANCELLED",
        order=order,
    )
    _broadcast_order_event(order, "order.updated")
    return order


# ???? ???? update_order_status ?????? ????? ?????? ?? ????? ????.
def update_order_status(order_id, cafe_id, new_status, user) -> Order:
    # ??? ??????? normalized_status ??? ????? ??? ???? ???? ???? ????? ????.
    normalized_status = (new_status or "").strip().upper()
    if normalized_status not in ALLOWED_ORDER_STATUS_TRANSITIONS:
        raise ValidationServiceError("Invalid order status.")

    with transaction.atomic():
        try:
            # ??? ??????? order ??? ????? ??? ???? ???? ???? ????? ????.
            order = (
                Order.objects.select_for_update()
                .for_cafe(cafe_id)
                .select_related("cafe", "user")
                .get(pk=order_id)
            )
        except Order.DoesNotExist as exc:
            raise NotFoundServiceError("Order not found.") from exc

        if getattr(user, "is_superuser", False) is False:
            # ??? ??????? owner_cafe ??? ????? ??? ???? ???? ???? ????? ????.
            owner_cafe = getattr(user, "my_cafe", None)
            if owner_cafe is None or owner_cafe.id != order.cafe_id:
                raise ValidationServiceError("You cannot update orders outside your cafe scope.")

        # ??? ??????? current_status ??? ????? ??? ???? ???? ???? ????? ????.
        current_status = order.status
        if current_status == normalized_status:
            return order

        # ??? ??????? allowed_targets ??? ????? ??? ???? ???? ???? ????? ????.
        allowed_targets = ALLOWED_ORDER_STATUS_TRANSITIONS.get(current_status, set())
        if normalized_status not in allowed_targets:
            raise ValidationServiceError(
                f"Invalid status transition from {current_status} to {normalized_status}."
            )

        order.status = normalized_status
        order.save(update_fields=["status", "updated_at"])
        if normalized_status == "CANCELLED" and _normalize_payment_method(
            order.payment_method
        ) in {PaymentMethod.WALLET, PaymentMethod.NFC}:
            _refund_wallet_for_cancelled_order(order)

    title, body = ORDER_STATUS_NOTIFICATIONS.get(
        normalized_status,
        ("تم تحديث الطلب", f"تم تحديث طلبك #{order.display_order_number}."),
    )
    send_real_notification(
        order.user,
        title,
        f"{body} رقم الطلب: {order.display_order_number}.",
        event_type=f"ORDER_{normalized_status}",
        order=order,
    )
    _broadcast_order_event(order, "order.updated")

    return order
