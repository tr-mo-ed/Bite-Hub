from __future__ import annotations

from collections.abc import Iterable

from django.db import models


# ???? ???? _resolve_scope_id ?????? ????? ?????? ?? ????? ????.
def _resolve_scope_id(scope: object) -> object:
    if scope is None:
        raise ValueError("Scope value cannot be None.")
    return getattr(scope, "pk", scope)


# ???? ???? _resolve_scope_ids ?????? ????? ?????? ?? ????? ????.
def _resolve_scope_ids(scopes: Iterable[object]) -> list[object]:
    return [_resolve_scope_id(scope) for scope in scopes]


