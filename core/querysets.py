class CafeQuerySet(models.QuerySet):
    # ???? ???? active ?????? ????? ?????? ?? ????? ????.
    def active(self) -> "CafeQuerySet":
        return self.filter(is_active=True)

    # ???? ???? for_faculty ?????? ????? ?????? ?? ????? ????.
    def for_faculty(self, faculty: object) -> "CafeQuerySet":
        # ??? ??????? faculty_id ??? ????? ??? ???? ???? ???? ????? ????.
        faculty_id = _resolve_scope_id(faculty)
        return self.filter(faculty_id=faculty_id)

    # ???? ???? owned_by ?????? ????? ?????? ?? ????? ????.
    def owned_by(self, user: object) -> "CafeQuerySet":
        if getattr(user, "is_superuser", False):
            return self
        return self.filter(owner=user)


