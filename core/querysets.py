class CafeScopedQuerySet(models.QuerySet):
    # ???? ???? for_cafe ?????? ????? ?????? ?? ????? ????.
    def for_cafe(self, cafe: object) -> "CafeScopedQuerySet":
        # ??? ??????? cafe_id ??? ????? ??? ???? ???? ???? ????? ????.
        cafe_id = _resolve_scope_id(cafe)
        return self.filter(cafe_id=cafe_id)

    # ???? ???? for_cafes ?????? ????? ?????? ?? ????? ????.
    def for_cafes(self, cafes: Iterable[object]) -> "CafeScopedQuerySet":
        # ??? ??????? cafe_ids ??? ????? ??? ???? ???? ???? ????? ????.
        cafe_ids = _resolve_scope_ids(cafes)
        return self.filter(cafe_id__in=cafe_ids)


