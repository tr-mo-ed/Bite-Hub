class Transaction(models.Model):
    # ??? ??????? TRANSACTION_TYPES ??? ????? ??? ???? ???? ???? ????? ????.
    TRANSACTION_TYPES = (
        ('DEPOSIT', 'إيداع (شحن)'),
        ('WITHDRAWAL', 'خصم (دفع)'),
    )
    # ??? ??????? SOURCES ??? ????? ??? ???? ???? ???? ????? ????.
    SOURCES = (
        ('SYSTEM', 'المنظومة'), 
        ('APP', 'التطبيق'),
        ('USER', 'المستخدم'),
        ('NFC', 'بطاقة NFC'),
    )

    # ??? ??????? id ?????? ??? ?????? ???? ??? ??????? ????.
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    # ??? ??????? wallet ?????? ??? ?????? ???? ??? ??????? ????.
    wallet = models.ForeignKey(Wallet, on_delete=models.CASCADE, related_name='transactions')
    cafe = models.ForeignKey(
        "core.Cafe",
        on_delete=models.SET_NULL,
        related_name="wallet_transactions",
        null=True,
        blank=True,
        db_index=True,
    )
    # ??? ??????? amount ?????? ??? ?????? ???? ??? ??????? ????.
    amount = models.DecimalField(max_digits=10, decimal_places=2, verbose_name="القيمة")
    # ??? ??????? transaction_type ?????? ??? ?????? ???? ??? ??????? ????.
    transaction_type = models.CharField(max_length=20, choices=TRANSACTION_TYPES)
    # ??? ??????? source ?????? ??? ?????? ???? ??? ??????? ????.
    source = models.CharField(max_length=20, choices=SOURCES, default='SYSTEM')
    # ??? ??????? description ?????? ??? ?????? ???? ??? ??????? ????.
    description = models.TextField(blank=True, null=True)
    # ??? ??????? created_at ?????? ??? ?????? ???? ??? ??????? ????.
    created_at = models.DateTimeField(auto_now_add=True)

    # ???? ???? save ?????? ????? ?????? ?? ????? ????.
    def save(self, *args, **kwargs):
        # Transaction Logic: Update wallet balance atomically
        with transaction.atomic():
            if self._state.adding:
                # Lock wallet row for update to prevent race conditions
                wallet = Wallet.objects.select_for_update().get(pk=self.wallet.pk)
                
                if self.transaction_type == 'DEPOSIT':
                    wallet.balance += self.amount
                elif self.transaction_type == 'WITHDRAWAL':
                    if wallet.balance >= self.amount:
                        wallet.balance -= self.amount
                    else:
                        raise ValueError("الرصيد لا يكفي لإتمام العملية!")
                
                wallet.save()
            
            super().save(*args, **kwargs)


class WalletDebitRequest(models.Model):
    class Status(models.TextChoices):
        PENDING = "PENDING", "بانتظار موافقة الطالب"
        APPROVED = "APPROVED", "وافق الطالب"
        REJECTED = "REJECTED", "رفض الطالب"

    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    wallet = models.ForeignKey(
        Wallet,
        on_delete=models.CASCADE,
        related_name="debit_requests",
    )
    cafe = models.ForeignKey(
        "core.Cafe",
        on_delete=models.CASCADE,
        related_name="wallet_debit_requests",
    )
    requested_by = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.SET_NULL,
        related_name="requested_wallet_debits",
        null=True,
        blank=True,
    )
    transaction_record = models.OneToOneField(
        Transaction,
        on_delete=models.SET_NULL,
        related_name="debit_request",
        null=True,
        blank=True,
    )
    amount = models.DecimalField(max_digits=10, decimal_places=2)
    note = models.CharField(max_length=255, blank=True)
    status = models.CharField(
        max_length=20,
        choices=Status.choices,
        default=Status.PENDING,
        db_index=True,
    )
    responded_at = models.DateTimeField(null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ("-created_at",)
        indexes = [
            models.Index(fields=("wallet", "status", "created_at")),
            models.Index(fields=("cafe", "status", "created_at")),
        ]

    def __str__(self):
        return f"{self.cafe_id} -> {self.wallet_id}: {self.amount} ({self.status})"

