from rest_framework import serializers
from .models import Transaction, Wallet, WalletDebitRequest

# ???? ???? TransactionSerializer ???? ?????? ????????? ???? ???? ?????.
class TransactionSerializer(serializers.ModelSerializer):
    # 1. تحسين العرض: إضافة التاريخ المنسق والنوع بالعربي
    created_at_formatted = serializers.SerializerMethodField()
    # ??? ??????? type_display ??? ????? ??? ???? ???? ???? ????? ????.
    type_display = serializers.CharField(source='get_transaction_type_display', read_only=True)
    
    # 2. اسم الكلية/المقهى (كما طلبت)
    college_name = serializers.SerializerMethodField()

    # ???? ???? Meta ???? ?????? ????????? ???? ???? ?????.
    class Meta:
        # ??? ??????? model ??? ????? ??? ???? ???? ???? ????? ????.
        model = Transaction
        # ??? ??????? fields ??? ????? ??? ???? ???? ???? ????? ????.
        fields = [
            'id', 
            'amount', 
            'transaction_type', 
            'type_display', 
            'source', 
            'description', 
            'created_at', 
            'created_at_formatted', 
            'college_name'
        ]

    # ???? ???? get_created_at_formatted ?????? ????? ?????? ?? ????? ????.
    def get_created_at_formatted(self, obj):
        # تنسيق: 2024-12-20 02:30 PM
        return obj.created_at.strftime("%Y-%m-%d %I:%M %p")

    # ???? ???? get_college_name ?????? ????? ?????? ?? ????? ????.
    def get_college_name(self, obj):
        if obj.cafe_id and obj.cafe:
            return obj.cafe.name
        return str(obj.wallet.college or "Bite Hub")


