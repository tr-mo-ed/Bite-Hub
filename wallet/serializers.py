class WalletSerializer(serializers.ModelSerializer):
    # نعرض آخر 10 معاملات فقط لتسريع التطبيق (بدلاً من جلب الآلاف)
    recent_transactions = serializers.SerializerMethodField()
    # ??? ??????? currency ??? ????? ??? ???? ???? ???? ????? ????.
    currency = serializers.SerializerMethodField()
    user_full_name = serializers.CharField(source='user.full_name', read_only=True)
    has_nfc_card = serializers.SerializerMethodField()
    nfc_card_last4 = serializers.SerializerMethodField()

    # ???? ???? Meta ???? ?????? ????????? ???? ???? ?????.
    class Meta:
        # ??? ??????? model ??? ????? ??? ???? ???? ???? ????? ????.
        model = Wallet
        # ??? ??????? fields ??? ????? ??? ???? ???? ???? ????? ????.
        fields = [
            'id',
            'balance',
            'currency',
            'college',
            'link_code',
            'user_full_name',
            'has_nfc_card',
            'nfc_card_last4',
            'updated_at',
            'recent_transactions',
        ]

    # ???? ???? get_currency ?????? ????? ?????? ?? ????? ????.
    def get_currency(self, obj):
        return "LYD"

    def get_has_nfc_card(self, obj):
        return bool(obj.nfc_card_uid)

    def get_nfc_card_last4(self, obj):
        return (obj.nfc_card_uid or '')[-4:]

    # ???? ???? get_recent_transactions ?????? ????? ?????? ?? ????? ????.
    def get_recent_transactions(self, obj):
        # جلب أحدث 10 عمليات فقط
        qs = Transaction.objects.filter(wallet=obj).order_by('-created_at')[:10]
        return TransactionSerializer(qs, many=True).data
