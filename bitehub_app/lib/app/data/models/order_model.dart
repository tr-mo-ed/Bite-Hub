class OrderModel {
  // ??? ??????? id ??? ?????? ???? ????? ????.
  final int id;
  // ??? ??????? orderNumber ??? ?????? ???? ????? ????.
  final String orderNumber;
  // ??? ??????? totalPrice ??? ?????? ???? ????? ????.
  final double totalPrice;
  // ??? ??????? status ??? ?????? ???? ????? ????.
  final String status;
  // ??? ??????? createdAt ??? ?????? ???? ????? ????.
  final String createdAt;
  final String? updatedAt;
  // ??? ??????? items ??? ?????? ???? ????? ????.
  final List<OrderItem> items;
  // ??? ??????? cafeId ??? ?????? ???? ????? ????.
  final String? cafeId;
  // ??? ??????? cafeName ??? ?????? ???? ????? ????.
  final String? cafeName;
  // ??? ??????? cafeLogo ??? ?????? ???? ????? ????.
  final String? cafeLogo;
  final String notes;
  final String paymentMethod;

  OrderModel({
    required this.id,
    required this.orderNumber,
    required this.totalPrice,
    required this.status,
    required this.createdAt,
    this.updatedAt,
    required this.items,
    this.cafeId,
    this.cafeName,
    this.cafeLogo,
    this.notes = '',
    this.paymentMethod = 'CASH',
  });

  OrderModel copyWith({
    int? id,
    String? orderNumber,
    double? totalPrice,
    String? status,
    String? createdAt,
    String? updatedAt,
    List<OrderItem>? items,
    String? cafeId,
    String? cafeName,
    String? cafeLogo,
    String? notes,
    String? paymentMethod,
  }) {
    return OrderModel(
      id: id ?? this.id,
      orderNumber: orderNumber ?? this.orderNumber,
      totalPrice: totalPrice ?? this.totalPrice,
      status: status ?? this.status,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
      items: items ?? this.items,
      cafeId: cafeId ?? this.cafeId,
      cafeName: cafeName ?? this.cafeName,
      cafeLogo: cafeLogo ?? this.cafeLogo,
      notes: notes ?? this.notes,
      paymentMethod: paymentMethod ?? this.paymentMethod,
    );
  }

  factory OrderModel.fromJson(Map<String, dynamic> json) {
    return OrderModel(
      id: json['id'] is int
          ? json['id']
          : int.tryParse(json['id'].toString()) ?? 0,
      orderNumber: json['order_number']?.toString() ?? '---',
      totalPrice: double.tryParse(json['total_price'].toString()) ?? 0.0,
      status: json['status'] ?? 'PENDING',
      createdAt: json['created_at'] ?? DateTime.now().toString(),
      updatedAt: json['updated_at']?.toString(),
      items: (json['items'] as List?)
              ?.map((i) => OrderItem.fromJson(i))
              .toList() ??
          [],
      cafeId: json['cafe_id']?.toString() ?? json['college_id']?.toString(),
      cafeName: json['cafe_name'] ?? json['college_name'] ?? 'مقهى غير معروف',
      cafeLogo: json['cafe_logo'] ?? json['image_url'],
      notes: json['notes']?.toString() ?? '',
      paymentMethod: json['payment_method']?.toString() ?? 'CASH',
    );
  }

  DateTime get dateObject {
    try {
      return DateTime.parse(createdAt);
    } catch (_) {
      return DateTime.now();
    }
  }

  DateTime? get updatedDateObject {
    final raw = updatedAt;
    if (raw == null || raw.trim().isEmpty) {
      return null;
    }
    try {
      return DateTime.parse(raw);
    } catch (_) {
      return null;
    }
  }

  String get displayCafeName {
    return (cafeName ?? '').trim();
  }

  String get displayOrderCode {
    final normalized = orderNumber.trim().toUpperCase();
    if (normalized.isEmpty || normalized == '---') {
      return id > 0 ? 'BH-${id.toString().padLeft(6, '0')}' : '---';
    }
    final digits = normalized.replaceAll(RegExp(r'\D'), '');
    if (digits.isEmpty) {
      return normalized;
    }
    final serial = digits.length >= 6
        ? digits.substring(digits.length - 6)
        : digits.padLeft(6, '0');
    if (normalized.startsWith('BH-')) {
      return 'BH-$serial';
    }
    return 'BH-$serial';
  }

  String get statusText {
    switch (status.toUpperCase()) {
      case 'PENDING':
        return 'قيد انتظار المراجعة';
      case 'ACCEPTED':
        return 'تم قبول طلبك';
      case 'PREPARING':
        return 'طلبك قيد التحضير';
      case 'READY':
        return 'طلبك جاهز للاستلام';
      case 'COMPLETED':
        return 'تم استلام الطلب';
      case 'CANCELLED':
        return 'تم إلغاء الطلب';
      default:
        return status;
    }
  }
}

