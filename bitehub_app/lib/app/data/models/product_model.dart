class ProductModel {
  // ??? ??????? id ??? ?????? ???? ????? ????.
  final String id;
  // ??? ??????? name ??? ?????? ???? ????? ????.
  final String name;
  // ??? ??????? price ??? ?????? ???? ????? ????.
  final double price;
  final double? originalPrice;
  final bool hasDiscount;
  final int discountPercentage;
  // ??? ??????? imageUrl ??? ?????? ???? ????? ????.
  final String imageUrl;
  // ??? ??????? description ??? ?????? ???? ????? ????.
  final String description;

  // ??? ??????? category ??? ?????? ???? ????? ????.
  final String category;
  // ??? ??????? categoryId ??? ?????? ???? ????? ????.
  final String categoryId;

  // ??? ??????? cafeId ??? ?????? ???? ????? ????.
  final String cafeId;
  // ??? ??????? cafeName ??? ?????? ???? ????? ????.
  final String cafeName;
  // ??? ??????? collegeId ??? ?????? ???? ????? ????.
  final String collegeId;
  // ??? ??????? collegeName ??? ?????? ???? ????? ????.
  final String collegeName;
  // ??? ??????? imageVariant ??? ?????? ???? ????? ????.
  final int? imageVariant;

  // ??? ??????? isAvailable ??? ?????? ???? ????? ????.
  final bool isAvailable;
  bool isFavorite;
  // ??? ??????? rating ??? ?????? ???? ????? ????.
  final double rating;
  // ??? ??????? ratingCount ??? ?????? ???? ????? ????.
  final int ratingCount;

  String get cafeteriaName => cafeName.isNotEmpty ? cafeName : collegeName;

  ProductModel({
    required this.id,
    required this.name,
    required this.price,
    this.originalPrice,
    this.hasDiscount = false,
    this.discountPercentage = 0,
    required this.imageUrl,
    required this.description,
    required this.category,
    required this.categoryId,
    required this.collegeId,
    required this.collegeName,
    this.imageVariant,
    this.cafeId = '',
    this.cafeName = '',
    this.isAvailable = true,
    this.isFavorite = false,
    this.rating = 0.0,
    this.ratingCount = 0,
  });

  factory ProductModel.fromJson(Map<String, dynamic> json) {
    final image = (json['image_url'] ?? json['image'] ?? json['imageUrl'] ?? '')
        .toString();

    String catName = 'فئة غير معروفة';
    String catId = '';
    if (json['category'] is Map) {
      catName = json['category']['name']?.toString() ?? 'فئة غير معروفة';
      catId = json['category']['id']?.toString() ?? '';
    } else {
      catName = (json['category_name'] ?? json['category'] ?? 'فئة غير معروفة')
          .toString();
      catId = json['category_id']?.toString() ?? '';
    }

    String cfName = (json['cafe_name'] ?? '').toString();
    String clgName = (json['college_name'] ?? '').toString();
    String cfId = (json['cafe_id'] ?? '').toString();
    String clgId =
        (json['college_id'] ?? json['college'] ?? cfId ?? '').toString();

    if (json['cafe'] is Map) {
      cfName = json['cafe']['name']?.toString() ?? cfName;
      cfId = json['cafe']['id']?.toString() ?? cfId;
    }

    return ProductModel(
      id: json['id'].toString(),
      name: (json['name'] ?? 'منتج').toString(),
      price: double.tryParse(json['price'].toString()) ?? 0.0,
      originalPrice: json['original_price'] == null
          ? null
          : double.tryParse(json['original_price'].toString()),
      hasDiscount: json['has_discount'] == true ||
          json['has_discount'].toString().toLowerCase() == 'true',
      discountPercentage:
          int.tryParse((json['discount_percentage'] ?? 0).toString()) ?? 0,
      imageUrl: image,
      description: (json['description'] ?? '').toString(),
      category: catName,
      categoryId: catId,
      cafeId: cfId,
      cafeName: cfName.isNotEmpty ? cfName : 'مقهى غير معروف',
      collegeId: clgId,
      collegeName: clgName.isNotEmpty ? clgName : cfName,
      imageVariant: json['image_variant'] is int
          ? json['image_variant']
          : int.tryParse((json['image_variant'] ?? '').toString()),
      isAvailable: json['is_available'] == null
          ? true
          : json['is_available'] == true ||
              json['is_available'].toString().toLowerCase() == 'true',
      isFavorite: json['is_favorite'] == true ||
          json['is_favorite'].toString().toLowerCase() == 'true',
      rating: double.tryParse(json['rating'].toString()) ?? 4.5,
      ratingCount: int.tryParse(json['rating_count'].toString()) ?? 50,
    );
  }

  // ???? ???? getImageUrl ???? ??????? ?? ????? ???? ?????? ?????.
  String getImageUrl() {
    return SmartImageUtil.getImagePath(
      name,
      imageUrl,
      category: category,
      imageVariant: imageVariant,
    );
  }
}
