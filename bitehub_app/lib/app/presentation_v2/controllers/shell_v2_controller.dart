import 'package:flutter/foundation.dart';
import 'package:bitehub_app/app/data/models/college_model.dart';
import 'package:bitehub_app/app/data/providers/auth_provider.dart';
import 'package:bitehub_app/app/data/services/api_service.dart';

// ???? ???? ShellV2Controller ???? ???? ????? ???? ?? ???? ????.
class ShellV2Controller extends ChangeNotifier {
  ShellV2Controller({
    required AuthProvider authProvider,
    ApiService? apiService,
  })  : _authProvider = authProvider,
        _apiService = apiService ?? ApiService();

  // ??? ??????? _authProvider ??? ?????? ???? ????? ????.
  final AuthProvider _authProvider;
  // ??? ??????? _apiService ??? ?????? ???? ????? ????.
  final ApiService _apiService;

  int _currentIndex = 0;
  bool _isLoadingCafes = false;
  String? _errorMessage;
  List<CollegeModel> _cafes = const [];

  int get currentIndex => _currentIndex;
  bool get isLoadingCafes => _isLoadingCafes;
  String? get errorMessage => _errorMessage;
  List<CollegeModel> get cafes => _cafes;
  AuthProvider get authProvider => _authProvider;

  // ???? ???? initialize ???? ??????? ?? ????? ???? ?????? ?????.
  Future<void> initialize() async {
    await refreshCafes();
  }

  // ???? ???? refreshCafes ???? ??????? ?? ????? ???? ?????? ?????.
  Future<void> refreshCafes() async {
    _isLoadingCafes = true;
    _errorMessage = null;
    notifyListeners();

    try {
      _cafes = await _apiService.getCafes();
    } catch (error) {
      _errorMessage = error.toString();
    } finally {
      _isLoadingCafes = false;
      notifyListeners();
    }
  }

  // ???? ???? setIndex ???? ??????? ?? ????? ???? ?????? ?????.
  void setIndex(int index) {
    if (_currentIndex == index) {
      return;
    }
    _currentIndex = index;
    notifyListeners();
  }

  // ???? ???? logout ???? ??????? ?? ????? ???? ?????? ?????.
  Future<void> logout() async {
    await _authProvider.logout();
  }
}


