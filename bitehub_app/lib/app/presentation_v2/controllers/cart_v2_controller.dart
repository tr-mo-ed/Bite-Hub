import 'package:flutter/foundation.dart';
import 'package:bitehub_app/app/data/models/cart_item_model.dart';
import 'package:bitehub_app/app/data/models/order_model.dart';
import 'package:bitehub_app/app/data/providers/cart_provider.dart';

// ???? ???? CartV2Controller ???? ???? ????? ???? ?? ???? ????.
class CartV2Controller extends ChangeNotifier {
  CartV2Controller({required CartProvider cartProvider})
      : _cartProvider = cartProvider {
    _cartProvider.addListener(_handleCartChanged);
  }

  // ??? ??????? _cartProvider ??? ?????? ???? ????? ????.
  final CartProvider _cartProvider;

  bool _isSubmitting = false;
  String _paymentMethod = 'WALLET';
  String? _errorMessage;

  List<CartItem> get items => _cartProvider.items.values.toList();
  double get totalAmount => _cartProvider.totalAmount;
  int get itemCount => _cartProvider.itemCount;
  bool get isSubmitting => _isSubmitting;
  String get paymentMethod => _paymentMethod;
  String? get errorMessage => _errorMessage;

  bool get isEmpty => _cartProvider.items.isEmpty;
  String? get cafeName =>
      isEmpty ? null : _cartProvider.items.values.first.collegeName;

  // ???? ???? _handleCartChanged ???? ??????? ?? ????? ???? ?????? ?????.
  void _handleCartChanged() => notifyListeners();

  // ???? ???? selectPaymentMethod ???? ??????? ?? ????? ???? ?????? ?????.
  void selectPaymentMethod(String value) {
    if (value != 'WALLET' || _paymentMethod == value) {
      return;
    }
    _paymentMethod = value;
    notifyListeners();
  }

  // ???? ???? incrementItem ???? ??????? ?? ????? ???? ?????? ?????.
  void incrementItem(String id) => _cartProvider.incrementItem(id);

  // ???? ???? decrementItem ???? ??????? ?? ????? ???? ?????? ?????.
  void decrementItem(String id) => _cartProvider.removeSingleItem(id);

  // ???? ???? removeItem ???? ??????? ?? ????? ???? ?????? ?????.
  void removeItem(String id) => _cartProvider.removeItem(id);

  // ???? ???? clear ???? ??????? ?? ????? ???? ?????? ?????.
  void clear() => _cartProvider.clear();

  // ???? ???? checkout ???? ??????? ?? ????? ???? ?????? ?????.
  Future<OrderModel?> checkout() async {
    if (isEmpty || _isSubmitting) {
      return null;
    }

    _isSubmitting = true;
    _errorMessage = null;
    notifyListeners();

    try {
      return await _cartProvider.checkout(paymentMethod: _paymentMethod);
    } catch (error) {
      _errorMessage = _friendlyOrderError(error);
      rethrow;
    } finally {
      _isSubmitting = false;
      notifyListeners();
    }
  }

  // ???? ???? _friendlyOrderError ???? ??????? ?? ????? ???? ?????? ?????.
  String _friendlyOrderError(Object error) {
    final message = error.toString();
    if (message.contains('401') ||
        message.contains('Not authenticated') ||
        message.contains('Unauthorized')) {
      return 'انتهت الجلسة. يرجى تسجيل الدخول مرة أخرى.';
    }
    if (message.contains('balance') ||
        message.contains('رصيد') ||
        message.contains('wallet')) {
      return 'رصيد المحفظة غير كافٍ لإتمام الطلب.';
    }
    if (message.contains('مقهى') ||
        message.contains('cafe') ||
        message.contains('single')) {
      return 'الطلب يجب أن يكون من مقهى واحد.';
    }
    return 'تعذر إرسال الطلب. حاول مرة أخرى.';
  }

  @override
  // ???? ???? dispose ???? ??????? ?? ????? ???? ?????? ?????.
  void dispose() {
    _cartProvider.removeListener(_handleCartChanged);
    super.dispose();
  }
}


